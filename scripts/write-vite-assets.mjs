import { existsSync, readFileSync, writeFileSync } from 'fs';

const manifestPath = 'public/build/manifest.json';

if (!existsSync(manifestPath)) {
    console.error(`Missing ${manifestPath}. Run vite build first.`);
    process.exit(1);
}

const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));
const css = new Set();
const js = new Set();

for (const entry of Object.values(manifest)) {
    if (typeof entry?.file !== 'string') {
        continue;
    }

    const href = `/build/${entry.file}`;

    if (entry.file.endsWith('.css')) {
        css.add(href);
    }

    if (entry.file.endsWith('.js')) {
        js.add(href);
    }
}

const php = `<?php

// Generated by scripts/write-vite-assets.mjs — do not edit.
return [
    'css' => ${JSON.stringify([...css], null, 4).replace(/^/gm, '    ')},
    'js' => ${JSON.stringify([...js], null, 4).replace(/^/gm, '    ')},
];
`;

writeFileSync('bootstrap/vite-assets.php', php);
console.log('Wrote bootstrap/vite-assets.php');
